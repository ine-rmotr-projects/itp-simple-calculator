def add(x, y):
    pass
    return x + y


def subtract(x, y):
    pass
    return x - y


def divide(x, y):
    pass
    if y == 0:
        return "Invalid value for denominator, cant't divide by 0!"
    else:
        return x / y


def multiply(x, y):
    pass
    return x * y


def square(x):
    pass
    return x ** 2


def power(x, y):
    pass
    return x ** y


